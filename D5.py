'''A comment is a line of code that is not executed by the program. 
   It is used to explain the code and make it easier to understand for other programmers who may read it in the future.'''

print("hey i am  a good boy")


"""escape sequence is a character that is used to represent a special character in a string.
It is used to represent characters that are not allowed in a string, such as a newline or a tab.
The most common escape sequences are:
\n - represents a newline
\t - represents a tab"""

print("Ravi is a good boy\nHe is a good student")



